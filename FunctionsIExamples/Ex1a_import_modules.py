## Using import moduleName
import math         # This will import math module
import random       # This will import random module

print("int(2.6) =", int(2.6))
print("chr(65) =", chr(65))
print("ord('A') =", ord('A'))
print("round(2.34, 1) =", round(2.34, 1))
print()

print("math.pi =", math.pi)
print("math.ceil(math.pi) =", math.ceil(math.pi))
print("math.floor(math.pi) =", math.floor(math.pi))
print("round(math.pi, 3) =", round(math.pi, 3))
print()

print( "math.pow(100, 2) =", math.pow(100, 2))
print( "math.pow(100, -2) =", math.pow(100, -2))
print( "Build-in pow(4.2) =", pow(4, 2))
print( "grade = ", random.randint(1, 100) )
print()
