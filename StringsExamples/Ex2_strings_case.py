fruit1 = "apple gala"
print(fruit1.capitalize() ) # capitalize() Converts the first character to upper case
print(fruit1.title() )     # title() Converts the first character of each word to upper case
print(fruit1.lower() )     # lower() converts the string to lowercase
print(fruit1.upper() )     # upper() converts the string to uppercase

name1 = fruit1.upper()
print(fruit1)
print(name1)
print()

