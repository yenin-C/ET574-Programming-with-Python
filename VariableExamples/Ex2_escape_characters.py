# Chap2 - Variables
# Using escape character \n & \t

# newline character \n, moves content to the next line
print("Hello\nWorld\n")

# tab character \t, adds a block of space
print("01234567890123456789")
print('Hello\tPython\tWorld\n')

# \" character, adds double quote
print("Hello \"Python\" World\n")
print('Hello "Python" World\n')

# \' character, adds single quote
print('Hello \'Python\' World\n')
print("Hello 'Python' World\n")
