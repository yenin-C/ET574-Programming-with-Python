# Chap2 - Variables
# Using constants

# Constants are variables where the name is uppercase
# In other languages constants cannot be changed.
# In Python, a constant is simply a variable that should not be modified.
PI = 3.14159265359
print("PI = " + str(PI))

NY_TAX_RATE = 1.08875
print("New York's tax rate =", NY_TAX_RATE)

GRAVITY = 9.81
print(f"The force of gravity Value = {GRAVITY} meters per second squared")
