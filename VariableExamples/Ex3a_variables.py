# Chap2 - Variables
# variable/operator/constant/function

# Variables are containers for storing data values
x = 2
x = x+2
print(x)
print()

# Using type() function
greet = 'Hello World'
number = 711
pi = 3.1415926535897932
print(greet, type(greet))
print(number, type(number))
print(pi, type(pi))
print()

# Update variables
greet = '711'
number = 7.11
pi = 'pi'
print(greet, type(greet))
print(number, type(number))
print(pi, type(pi))
