# Chap2 - Variables
# Rules to define variable names

Class = "Python"        # class is a reserved word
print(Class)            # Class is different from class, case sensitive

my_class = "Python"     # names can use underscores _ instead of spaces
print(my_class)

_myclass = "Python"     # names can start with underscores
print(_myclass)

# with multiple words, names can use proper case after the first word
myClass = "Python"
print(myClass)

# names can use numbers if they are NOT the first letter
class123 = "Python"
print(class123)
