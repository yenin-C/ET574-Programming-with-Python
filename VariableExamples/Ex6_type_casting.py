# Chap2 - Variables
# Using constructor functions to accomplish type casting

# type casting from float to integer
x = int(2.8)
print(x,type(x))

# type casting from string to integer
x = int("3")
print(x,type(x))

# type casting from integer to float
y = float(1)
print(y,type(y))

# type casting from string to float
y = float("3.8")
print(y,type(y))

#type casting from integer to string
z = str(1)
print(z,type(z))

#type casting from float to string
z = str(1.0)
print(z,type(z))
